export type SoundType = 'received' | 'sent' | 'destroy';

// ── Precarica tutti e tre al caricamento del modulo ───────────────────────────
function mkAudio(src: string): HTMLAudioElement {
  const a = new Audio(src);
  a.preload = 'auto';
  // iOS Safari richiede che l'elemento sia nel DOM per poter essere
  // sbloccato con play() e riprodotto in seguito senza user gesture.
  try {
    a.style.cssText = 'position:fixed;width:1px;height:1px;opacity:0;pointer-events:none';
    document.body.appendChild(a);
  } catch { /* SSR guard */ }
  a.load();
  return a;
}

const _sounds: Record<SoundType, HTMLAudioElement> = {
  received: mkAudio(SRC_RECEIVED),
  sent:     mkAudio(SRC_SENT),
  destroy:  mkAudio(SRC_DESTROY),
};

let _unlocked = false;

// ── Unlock: chiamare nel primo gesture handler ────────────────────────────────
export async function unlockNotifAudio(): Promise<void> {
  if (_unlocked) return;
  _unlocked = true;   // setta PRIMA dell'await — playNotifSound non aspetta

  // Sblocca TUTTI i suoni di notifica — non abbandonare se uno fallisce
  for (const a of Object.values(_sounds)) {
    if (!a) continue;
    const prev = a.volume;
    a.volume = 0;
    try { await a.play(); a.pause(); a.currentTime = 0; } catch { /* ignora */ }
    a.volume = prev > 0 ? prev : 1;
  }

  // Sblocca TUTTI i ring elements (multi-ringtone)
  for (const el of _ringEls.values()) {
    if (!el) continue;
    el.volume = 0;
    try { await el.play(); el.pause(); el.currentTime = 0; } catch { /* ignora */ }
    el.volume = 1;
  }

  console.info('[notifSound] audio unlocked ✓');
}

// ── Riproduzione ──────────────────────────────────────────────────────────────
export async function playNotifSound(type: SoundType = 'received'): Promise<void> {
  const a = _sounds[type];
  a.currentTime = 0;
  a.volume = 1;
  try {
    await a.play();
  } catch (err) {
    console.error('[notifSound] play() rejected for type='+type+':', err);
  }
}

// ── Squillo chiamata in arrivo ────────────────────────────────────────────────
// FIX: generato SINCRONAMENTE (senza OfflineAudioContext async).
// Problema precedente: OfflineAudioContext.startRendering() è asincrono —
// quando unlockNotifAudio() veniva chiamato al primo gesto dell'utente,
// _ringEl era ancora null → il ring element non veniva mai pre-sbloccato →
// _ringEl.play() falliva silenziosamente su iOS.
// Soluzione: generare il WAV con puro JavaScript sincrono → _ringEl esiste
// già al caricamento del modulo → disponibile quando arriva il primo gesto.

function _buildRingDataUrl(): string {
  const sampleRate = 22050;
  // Due impulsi da 0.3 s ciascuno (440 Hz + 480 Hz) → suono di "squillo"
  const pulseDur = 0.3;
  const silDur   = 0.15;
  const numPulse = Math.ceil(sampleRate * pulseDur);
  const numSil   = Math.ceil(sampleRate * silDur);
  const numTotal = numPulse + numSil;
  const dataSize = numTotal * 2;
  const ab = new ArrayBuffer(44 + dataSize);
  const v  = new DataView(ab);
  const ws = (off: number, s: string) => {
    for (let i = 0; i < s.length; i++) v.setUint8(off + i, s.charCodeAt(i));
  };
  // WAV header
  ws(0, "RIFF"); v.setUint32(4, 36 + dataSize, true);
  ws(8, "WAVE"); ws(12, "fmt "); v.setUint32(16, 16, true);
  v.setUint16(20, 1, true); v.setUint16(22, 1, true);
  v.setUint32(24, sampleRate, true); v.setUint32(28, sampleRate * 2, true);
  v.setUint16(32, 2, true); v.setUint16(34, 16, true);
  ws(36, "data"); v.setUint32(40, dataSize, true);
  // Campioni: tono durante il pulse, silenzio nella pausa
  for (let i = 0; i < numTotal; i++) {
    let s16 = 0;
    if (i < numPulse) {
      const t   = i / sampleRate;
      const env = i < numPulse * 0.05
        ? i / (numPulse * 0.05)                       // attack 5%
        : Math.exp(-(t - pulseDur * 0.05) * 5);       // decay
      const wave = (Math.sin(2 * Math.PI * 440 * t) +
                    Math.sin(2 * Math.PI * 480 * t)) * 0.5;
      s16 = Math.max(-32767, Math.min(32767, Math.round(wave * env * 0.45 * 32767)));
    }
    v.setInt16(44 + i * 2, s16, true);
  }
  // WAV → base64 in chunk da 8 KB (evita stack overflow su array grandi)
  const bytes = new Uint8Array(ab);
  let bin = "";
  const CHUNK = 8192;
  for (let i = 0; i < bytes.length; i += CHUNK) {
    bin += String.fromCharCode(...Array.from(bytes.subarray(i, Math.min(i + CHUNK, bytes.length))));
  }
  return "data:audio/wav;base64," + btoa(bin);
}

